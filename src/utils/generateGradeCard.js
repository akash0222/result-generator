import jsPDF from 'jspdf'

import autoTable
from 'jspdf-autotable'

const generateGradeCard = (

  student,
  marks,
  sgpa,
  cgpa

) => {

  const doc =
    new jsPDF()

  // ======================
  // COLORS
  // ======================

  const primaryColor =
    [41, 98, 255]

  const darkColor =
    [40, 40, 40]

  // ======================
  // HEADER
  // ======================

  doc.setFillColor(
    ...primaryColor
  )

  doc.rect(
    0,
    0,
    220,
    35,
    'F'
  )

  // COLLEGE NAME
  doc.setTextColor(255, 255, 255)

  doc.setFontSize(22)

  doc.setFont(
    'helvetica',
    'bold'
  )

  doc.text(

    'IILM Institute for Higher Education',

    18,

    18
  )

  // TITLE
  doc.setFontSize(16)

  doc.text(
    'OFFICIAL GRADE CARD',
    65,
    28
  )

  // RESET COLOR
  doc.setTextColor(
    ...darkColor
  )

  // ======================
  // STUDENT INFO
  // ======================

  doc.setFontSize(12)

  doc.setFont(
    'helvetica',
    'normal'
  )

  doc.text(
    `Student Name : ${student.name}`,
    20,
    50
  )

  doc.text(
    `Roll Number : ${student.roll}`,
    20,
    60
  )

  doc.text(
    `Course : ${student.course}`,
    20,
    70
  )

  doc.text(
    `Semester : ${
      student.semester || 1
    }`,
    20,
    80
  )

  doc.text(

    `Generated On : ${new Date().toLocaleDateString()}`,

    120,

    50
  )

  // ======================
  // TABLE DATA
  // ======================

  const tableData =
    marks.map((mark) => [

      mark.subject,

      mark.internal,

      mark.midterm,

      mark.endterm,

      mark.total,

      mark.grade
    ])

  // ======================
  // MARKS TABLE
  // ======================

  autoTable(doc, {

    startY: 95,

    head: [[

      'Subject',

      'Internal',

      'Midterm',

      'Endterm',

      'Total',

      'Grade'
    ]],

    body: tableData,

    styles: {

      fontSize: 10,

      halign: 'center'
    },

    headStyles: {

      fillColor:
        primaryColor,

      textColor:
        [255, 255, 255],

      fontStyle:
        'bold'
    },

    alternateRowStyles: {

      fillColor:
        [245, 245, 245]
    }
  })

  // ======================
  // GPA SECTION
  // ======================

  const finalY =
    doc.lastAutoTable.finalY

  doc.setFontSize(14)

  doc.setFont(
    'helvetica',
    'bold'
  )

  doc.setTextColor(
    ...primaryColor
  )

  doc.text(
    `SGPA : ${sgpa}`,
    20,
    finalY + 20
  )

  doc.text(
    `CGPA : ${cgpa}`,
    20,
    finalY + 32
  )

  // ======================
  // REMARKS
  // ======================

  let remarks =
    'Pass'

  if (cgpa >= 9) {

    remarks =
      'Outstanding Performance'
  }

  else if (cgpa >= 8) {

    remarks =
      'Excellent Performance'
  }

  else if (cgpa >= 7) {

    remarks =
      'Very Good Performance'
  }

  else if (cgpa >= 6) {

    remarks =
      'Good Performance'
  }

  else {

    remarks =
      'Needs Improvement'
  }

  doc.setTextColor(
    ...darkColor
  )

  doc.setFontSize(12)

  doc.text(
    `Academic Remarks : ${remarks}`,
    20,
    finalY + 48
  )

  // ======================
  // SIGNATURE
  // ======================

  doc.line(
    140,
    finalY + 55,
    190,
    finalY + 55
  )

  doc.text(
    'Controller of Examination',
    140,
    finalY + 63
  )

  // ======================
  // FOOTER
  // ======================

  doc.setFontSize(10)

  doc.setTextColor(
    120,
    120,
    120
  )

  doc.text(

    'This is a system generated grade card.',

    20,

    285
  )

  doc.text(

    'Generated by Result Management ERP',

    20,

    292
  )

  // ======================
  // RETURN PDF
  // ======================

  return doc.output(
    'blob'
  )
}

export default generateGradeCard