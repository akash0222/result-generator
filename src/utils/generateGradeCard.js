import jsPDF from 'jspdf'
import autoTable from 'jspdf-autotable'

const generateGradeCard = (
  student,
  marks,
  sgpa,
  cgpa
) => {

  const doc =
    new jsPDF()

  // HEADER
  doc.setFontSize(22)

  doc.text(
    'IILM Institute for Higher Education',
    25,
    20
  )

  doc.setFontSize(18)

  doc.text(
    'Official Grade Card',
    70,
    35
  )

  // STUDENT DETAILS
  doc.setFontSize(12)

  doc.text(
    `Name: ${student.name}`,
    20,
    55
  )

  doc.text(
    `Roll Number: ${student.roll}`,
    20,
    65
  )

  doc.text(
    `Course: ${student.course}`,
    20,
    75
  )

  // TABLE DATA
  const tableData =
    marks.map((mark) => [

      mark.subject,
      mark.internal,
      mark.midterm,
      mark.endterm,
      mark.total,
      mark.grade
    ])

  // TABLE
  autoTable(doc, {

    startY: 90,

    head: [[
      'Subject',
      'Internal',
      'Midterm',
      'Endterm',
      'Total',
      'Grade'
    ]],

    body: tableData
  })

  // SGPA & CGPA
  doc.setFontSize(14)

  doc.text(
    `SGPA: ${sgpa}`,
    20,
    doc.lastAutoTable.finalY + 20
  )

  doc.text(
    `CGPA: ${cgpa}`,
    20,
    doc.lastAutoTable.finalY + 30
  )

  // FOOTER
  doc.setFontSize(10)

  doc.text(
    'Generated by Result Management ERP',
    20,
    285
  )

  // RETURN PDF BLOB
  return doc.output('blob')
}

export default generateGradeCard